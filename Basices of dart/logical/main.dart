void main() {
  // * logical AND --> &&
  print("logical and:");
  print("true && false= ${true && false}");
  print("true && true= ${true && true}");

  // * logical OR --> ||
  print("logical and:");
  print("true || false= ${true || false}");
  print("true || true= ${true || true}");

  // * logical NOT --> !
  print(!true);
  print(!false);
}
