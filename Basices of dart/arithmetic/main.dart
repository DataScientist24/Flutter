void main() {
  int num1, num2;
  num1 = 10;
  num2 = 3;

  // * addition
  print("addition $num1 + $num2: ${num1 + num2} ");

  // * subtraction
  print("subtraction $num1 - $num2: ${num1 - num2} ");

  // * multiplication
  print("multiplication $num1 * $num2: ${num1 * num2} ");

  // * division
  print("division $num1 / $num2: ${num1 / num2}");
}
